    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int fila = jTable1.getSelectedRow();
       if (fila < 0){
           JOptionPane.showMessageDialog(this, "no hay fila seleccionada");
       }
       else{
           String nombre = jTable1.getModel().getValueAt(fila, 1).toString();
           JOptionPane.showMessageDialog(this, nombre);
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nombre = jTextField1.getText();
        String telefono = jTextField2.getText();
        if(nombre.length() > 0 || telefono.length() > 0){
            Object fila[] = new Object[2];
            fila[0] = jTextField1.getText();
            fila[1] = jTextField2.getText();
            datos.addRow(fila);
            jTable1.setModel(datos);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       int fila = jTable1.getSelectedRow();
       if (fila < 0){
           JOptionPane.showMessageDialog(this, "no hay fila seleccionada");
       }
       else{   
            String nombre = jTextField1.getText();
            String telefono = jTextField2.getText();
            datos.setValueAt(nombre, fila, 0);
            datos.setValueAt(telefono, fila, 1);
       }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       int fila = jTable1.getSelectedRow();
       if (fila < 0){
           JOptionPane.showMessageDialog(this, "no hay fila seleccionada");
       }
       else{   
            datos.removeRow(fila);
       }
    }//GEN-LAST:event_jButton5ActionPerformed

